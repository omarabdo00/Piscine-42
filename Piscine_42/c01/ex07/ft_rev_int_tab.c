#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	x;
	int	y;

	x = 0;
	y = size - 1;
	while (x < y)
	{
		temp = tab[x];
		tab[x] = tab[y];
		tab[y] = temp;
		x++;
		y--;
	}
}
